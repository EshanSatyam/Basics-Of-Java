
/* Java program to illustrate the application of Integer literals */

public class Integerliterals {
    public static void main(String[] args) {
        
        // decimal-form literal
        int a = 101;
          // octal-form literal
        int b = 0100;
        // Hexa-decimal form literal
        int c = 0xFace;
          // Binary literal
        int d = 0b1111;
       
        System.out.println("decimal-form literal : " + a);
        System.out.println("octal-form literal : " + b);
        System.out.println("Hexa-decimal form literal : " + c);
        System.out.println("Binary literal : " + d);

    }
}
